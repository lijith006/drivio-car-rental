class Car {
  final String id;
  final String name;
  final double pricePerDay;
  final String image;
  final bool isAvailable;
  final String transmission;
  final int seats;

  Car({
    required this.id,
    required this.name,
    required this.pricePerDay,
    required this.image,
    required this.isAvailable,
    required this.transmission,
    required this.seats,
  });
}
